#!/usr/bin/env python3
"""Watch the active Claude Code session JSONL and print a talkable summary
of each new assistant turn, generated by a local Ollama model."""

from __future__ import annotations

import asyncio
import hashlib
import json
import subprocess
import sys
import threading
import time
from pathlib import Path

import summarizer
from summarizer import ensure_server, rewarm_async, summarize, verbalize, warm_model

REPO_DIR = Path(__file__).resolve().parent.parent
PROJECTS_ROOT = Path.home() / ".claude/projects"
POLL_SECONDS = 0.5

# British voices only (b* = British English).
BRITISH_VOICES = [
    "bf_alice",
    "bf_emma",
    "bf_isabella",
    "bf_lily",
    "bm_daniel",
    "bm_fable",
    "bm_george",
    "bm_lewis",
]
KOKORO_MODEL = str(REPO_DIR / "kokoro-v1.0.onnx")
KOKORO_VOICES = str(REPO_DIR / "voices-v1.0.bin")
# Playback speed multiplier for synthesized speech (1.0 = normal, >1 faster).
KOKORO_SPEED = 1.3

# Voice for the current session; picked deterministically from the session id
# (the JSONL filename) so a conversation keeps one voice across restarts.
_voice = BRITISH_VOICES[0]


def voice_for_session(session: Path) -> str:
    """Pick a stable British voice from the session id (JSONL filename stem).

    Uses SHA-256 rather than the built-in hash(), which is salted per process
    and would give a different voice on every run."""
    session_id = session.stem
    digest = hashlib.sha256(session_id.encode()).digest()
    index = int.from_bytes(digest, "big") % len(BRITISH_VOICES)
    return BRITISH_VOICES[index]


_scope_dir: Path | None = None


def resolve_scope(query: str) -> Path:
    """Find the project dir under PROJECTS_ROOT whose name best matches `query`.

    Picks the shortest dir name containing `query` (case-insensitive); ties
    broken by most-recently-modified."""
    q = query.lower()
    candidates = [
        d for d in PROJECTS_ROOT.iterdir() if d.is_dir() and q in d.name.lower()
    ]
    if not candidates:
        sys.exit(f"[tts] no project under {PROJECTS_ROOT} matches {query!r}")
    candidates.sort(key=lambda d: (len(d.name), -d.stat().st_mtime))
    return candidates[0]


def latest_session_file() -> Path:
    pattern = "*.jsonl" if _scope_dir else "*/*.jsonl"
    root = _scope_dir or PROJECTS_ROOT
    files = sorted(root.glob(pattern), key=lambda p: p.stat().st_mtime)
    if not files:
        sys.exit(f"[tts] no .jsonl files under {root}")
    return files[-1]


def extract_assistant_text(line: str) -> str | None:
    try:
        rec = json.loads(line)
    except json.JSONDecodeError:
        return None
    if rec.get("type") != "assistant":
        return None
    msg = rec.get("message") or {}
    parts = []
    for block in msg.get("content") or []:
        if block.get("type") == "text":
            parts.append(block.get("text", ""))
    text = "\n".join(p for p in parts if p).strip()
    return text or None


def last_assistant_text(path: Path) -> str | None:
    last = None
    with path.open("r", encoding="utf-8", errors="replace") as fh:
        for line in fh:
            t = extract_assistant_text(line)
            if t:
                last = t
    return last


def follow(path: Path):
    """Yield new lines as they're appended. Re-opens if the active file rotates."""
    current = path
    fh = current.open("r", encoding="utf-8", errors="replace")
    fh.seek(0, 2)  # skip backlog; only react to new turns
    while True:
        line = fh.readline()
        if line:
            yield line
            continue
        time.sleep(POLL_SECONDS)
        newest = latest_session_file()
        if newest != current:
            global _voice
            _voice = voice_for_session(newest)
            print(f"[tts] switched to {newest.name} (voice: {_voice})", file=sys.stderr)
            fh.close()
            current = newest
            fh = current.open("r", encoding="utf-8", errors="replace")
            fh.seek(0, 2)


_kokoro = None
_playback_proc: subprocess.Popen | None = None
_playback_thread: threading.Thread | None = None
_playback_lock = threading.Lock()


def load_kokoro():
    global _kokoro
    if _kokoro is not None:
        return _kokoro
    print("[tts] loading kokoro model...", file=sys.stderr)
    from kokoro_onnx import Kokoro

    _kokoro = Kokoro(KOKORO_MODEL, KOKORO_VOICES)
    print("[tts] warming kokoro...", file=sys.stderr)
    _kokoro.create("ok", voice=_voice)
    return _kokoro


def play_tts(text: str) -> None:
    global _playback_thread
    _playback_thread = threading.Thread(target=_play_worker, args=(text,), daemon=True)
    _playback_thread.start()


def _stop_current_playback() -> None:
    """Kill any in-flight ffplay child so a new utterance can take over."""
    global _playback_proc
    with _playback_lock:
        proc = _playback_proc
        _playback_proc = None
    if proc and proc.poll() is None:
        proc.terminate()
        try:
            proc.wait(timeout=1)
        except subprocess.TimeoutExpired:
            proc.kill()


def _spawn_ffplay(sample_rate: int) -> subprocess.Popen:
    """Start ffplay reading f32 mono PCM from stdin at sample_rate."""
    return subprocess.Popen(
        [
            "ffplay",
            "-loglevel",
            "quiet",
            "-nodisp",
            "-autoexit",
            "-f",
            "f32le",
            "-ar",
            str(sample_rate),
            "-ch_layout",
            "mono",
            "-i",
            "-",
        ],
        stdin=subprocess.PIPE,
        stdout=subprocess.DEVNULL,
        stderr=subprocess.DEVNULL,
    )


def _play_worker(text: str) -> None:
    global _playback_proc

    async def stream() -> None:
        global _playback_proc
        kokoro = load_kokoro()
        proc: subprocess.Popen | None = None
        async for samples, sample_rate in kokoro.create_stream(
            text, voice=_voice, speed=KOKORO_SPEED
        ):
            if proc is None:
                # First chunk ready — interrupt any prior playback and start ours.
                _stop_current_playback()
                proc = _spawn_ffplay(sample_rate)
                with _playback_lock:
                    _playback_proc = proc
            try:
                proc.stdin.write(samples.astype("float32").tobytes())
                proc.stdin.flush()
            except (BrokenPipeError, ValueError):
                return
        if proc is not None and proc.stdin is not None:
            try:
                proc.stdin.close()
            except BrokenPipeError:
                pass

    try:
        asyncio.run(stream())
    except Exception as e:
        print(f"[tts] streaming playback failed: {e}", file=sys.stderr)


def speak(text: str, label: str) -> None:
    snippet = text.replace("\n", " ")[:80]
    print(f"[tts] {label}: {snippet}{'...' if len(text) > 80 else ''}", file=sys.stderr)
    try:
        summary = summarize(text)
    except Exception as e:
        print(f"[tts] summarize failed: {e}", file=sys.stderr)
        return
    rewarm_async()
    if not summary:
        return
    summary = verbalize(summary)
    print(summary, flush=True)
    try:
        play_tts(summary)
    except Exception as e:
        print(f"[tts] kokoro failed: {e}", file=sys.stderr)


def shutdown() -> None:
    """Stop audio playback and the ollama server we started (if any)."""
    _stop_current_playback()
    summarizer.shutdown()


def main() -> None:
    global _voice, _scope_dir
    if len(sys.argv) > 1:
        _scope_dir = resolve_scope(sys.argv[1])
        print(f"[tts] scoped to {_scope_dir.name}", file=sys.stderr)
    ensure_server()
    warm_model()
    session = latest_session_file()
    _voice = voice_for_session(session)
    load_kokoro()
    print(f"[tts] watching {session.name} (voice: {_voice})", file=sys.stderr)
    print("[tts] ready, waiting for new turns", file=sys.stderr)
    for line in follow(session):
        text = extract_assistant_text(line)
        if not text:
            continue
        speak(text, "new")


if __name__ == "__main__":
    try:
        main()
    except KeyboardInterrupt:
        pass
    finally:
        shutdown()
