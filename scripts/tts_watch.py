#!/usr/bin/env python3
"""Watch the active Claude Code session JSONL and print a talkable summary
of each new assistant turn, generated by a local Ollama model."""

from __future__ import annotations

import hashlib
import json
import os
import re
import subprocess
import sys
import threading
import time
import urllib.request
from datetime import datetime, timezone
from pathlib import Path

MODEL = "qwen3.5:2b-mlx"
OLLAMA_URL = "http://localhost:11434"
REPO_DIR = Path(__file__).resolve().parent.parent
PROJECT_DIR = Path.home() / ".claude/projects/-Users-pawelparadysz-code-claude-code-tts"
POLL_SECONDS = 0.5

LOG_DIR = REPO_DIR / "logs"
LOG_FILE = LOG_DIR / f"ollama-{datetime.now().strftime('%Y%m%d')}.jsonl"
_log_lock = threading.Lock()

# British voices only (b* = British English).
BRITISH_VOICES = [
    "bf_alice",
    "bf_emma",
    "bf_isabella",
    "bf_lily",
    "bm_daniel",
    "bm_fable",
    "bm_george",
    "bm_lewis",
]
KOKORO_MODEL = str(REPO_DIR / "kokoro-v1.0.onnx")
KOKORO_VOICES = str(REPO_DIR / "voices-v1.0.bin")
# Playback speed multiplier for synthesized speech (1.0 = normal, >1 faster).
KOKORO_SPEED = 1.3

# Voice for the current session; picked deterministically from the session id
# (the JSONL filename) so a conversation keeps one voice across restarts.
_voice = BRITISH_VOICES[0]


def voice_for_session(session: Path) -> str:
    """Pick a stable British voice from the session id (JSONL filename stem).

    Uses SHA-256 rather than the built-in hash(), which is salted per process
    and would give a different voice on every run."""
    session_id = session.stem
    digest = hashlib.sha256(session_id.encode()).digest()
    index = int.from_bytes(digest, "big") % len(BRITISH_VOICES)
    return BRITISH_VOICES[index]


SYSTEM_PROMPT = (
    "You turn an assistant's response into one or two short, spoken sentences. "
    "Rules: speak in plain conversational English; never read URLs, file paths, "
    "code, identifiers, or shell commands aloud (say 'a link', 'the config file', "
    "'the function', etc.); skip greetings and meta-talk; just say what changed "
    "or what was found. Output the spoken summary only, no preamble."
)


def log_io(
    kind: str, payload: dict, response: dict | None, error: str | None = None
) -> None:
    LOG_DIR.mkdir(exist_ok=True)
    record = {
        "ts": datetime.now(timezone.utc).isoformat(),
        "kind": kind,
        "request": payload,
        "response": response,
        "error": error,
    }
    line = json.dumps(record, ensure_ascii=False) + "\n"
    with _log_lock:
        with LOG_FILE.open("a", encoding="utf-8") as fh:
            fh.write(line)


# The ollama server process we started, if any. Only set when this script
# launches ollama itself, so shutdown never kills a pre-existing server.
_ollama_proc: subprocess.Popen | None = None


def ensure_server() -> None:
    global _ollama_proc
    try:
        urllib.request.urlopen(f"{OLLAMA_URL}/api/version", timeout=1).read()
        return
    except Exception:
        pass
    print("[tts] starting ollama server...", file=sys.stderr)
    _ollama_proc = subprocess.Popen(
        ["ollama", "serve"],
        stdout=subprocess.DEVNULL,
        stderr=subprocess.DEVNULL,
        env={**os.environ, "OLLAMA_NUM_PARALLEL": "1"},
    )
    for _ in range(40):
        try:
            urllib.request.urlopen(f"{OLLAMA_URL}/api/version", timeout=1).read()
            return
        except Exception:
            time.sleep(0.25)
    sys.exit("[tts] could not reach ollama at localhost:11434")


def warm_model() -> None:
    print(f"[tts] warming {MODEL}...", file=sys.stderr)
    summarize("hello")


def latest_session_file() -> Path:
    files = sorted(PROJECT_DIR.glob("*.jsonl"), key=lambda p: p.stat().st_mtime)
    if not files:
        sys.exit(f"[tts] no .jsonl files in {PROJECT_DIR}")
    return files[-1]


def extract_assistant_text(line: str) -> str | None:
    try:
        rec = json.loads(line)
    except json.JSONDecodeError:
        return None
    if rec.get("type") != "assistant":
        return None
    msg = rec.get("message") or {}
    parts = []
    for block in msg.get("content") or []:
        if block.get("type") == "text":
            parts.append(block.get("text", ""))
    text = "\n".join(p for p in parts if p).strip()
    return text or None


def summarize(text: str) -> str:
    payload = {
        "model": MODEL,
        "system": SYSTEM_PROMPT,
        "prompt": text,
        "stream": False,
        "think": False,
        "keep_alive": 0,
        "options": {
            "temperature": 0.7,
            "top_p": 0.8,
            "num_predict": 200,
            "num_ctx": 2048,
        },
    }
    req = urllib.request.Request(
        f"{OLLAMA_URL}/api/generate",
        data=json.dumps(payload).encode(),
        headers={"Content-Type": "application/json"},
    )
    try:
        with urllib.request.urlopen(req, timeout=120) as resp:
            body = json.loads(resp.read())
    except Exception as e:
        log_io("summarize", payload, None, error=repr(e))
        raise
    log_io("summarize", payload, body)
    return (body.get("response") or "").strip()


def rewarm_async() -> None:
    """Fire-and-forget reload so the next turn finds a warm model.

    Runs in parallel with TTS playback, so latency is hidden as long as
    playback takes longer than model load (~1-2s)."""

    def _warm():
        payload = {
            "model": MODEL,
            "prompt": "hi",
            "stream": False,
            "options": {"num_predict": 1, "num_ctx": 2048},
        }
        req = urllib.request.Request(
            f"{OLLAMA_URL}/api/generate",
            data=json.dumps(payload).encode(),
            headers={"Content-Type": "application/json"},
        )
        try:
            with urllib.request.urlopen(req, timeout=60) as resp:
                body = json.loads(resp.read())
            log_io("rewarm", payload, body)
        except Exception as e:
            log_io("rewarm", payload, None, error=repr(e))
            print(f"[tts] rewarm failed: {e}", file=sys.stderr)

    threading.Thread(target=_warm, daemon=True).start()


def last_assistant_text(path: Path) -> str | None:
    last = None
    with path.open("r", encoding="utf-8", errors="replace") as fh:
        for line in fh:
            t = extract_assistant_text(line)
            if t:
                last = t
    return last


def follow(path: Path):
    """Yield new lines as they're appended. Re-opens if the active file rotates."""
    current = path
    fh = current.open("r", encoding="utf-8", errors="replace")
    fh.seek(0, 2)  # skip backlog; only react to new turns
    while True:
        line = fh.readline()
        if line:
            yield line
            continue
        time.sleep(POLL_SECONDS)
        newest = latest_session_file()
        if newest != current:
            global _voice
            _voice = voice_for_session(newest)
            print(f"[tts] switched to {newest.name} (voice: {_voice})", file=sys.stderr)
            fh.close()
            current = newest
            fh = current.open("r", encoding="utf-8", errors="replace")
            fh.seek(0, 2)


_kokoro = None
_stop_playback = threading.Event()
_playback_thread: threading.Thread | None = None


def load_kokoro():
    global _kokoro
    if _kokoro is not None:
        return _kokoro
    print("[tts] loading kokoro model...", file=sys.stderr)
    from kokoro_onnx import Kokoro

    _kokoro = Kokoro(KOKORO_MODEL, KOKORO_VOICES)
    print("[tts] warming kokoro...", file=sys.stderr)
    _kokoro.create("ok", voice=_voice)
    return _kokoro


def verbalize(text: str) -> str:
    """Make dotted tokens read better aloud.

    "4.8" -> "4 point 8" (handles 4.8.2 too); other word.word dots like
    "config.yaml" become spaces so filenames aren't read as one word."""
    text = re.sub(r"(\d)\.(?=\d)", r"\1 point ", text)
    text = re.sub(r"(\w)\.(?=\w)", r"\1 ", text)
    return text


def play_tts(text: str) -> None:
    global _playback_thread
    _playback_thread = threading.Thread(target=_play_worker, args=(text,), daemon=True)
    _playback_thread.start()


def _reset_portaudio(sd) -> None:
    """Re-initialize PortAudio so it re-scans the current audio devices.

    PortAudio snapshots the device list at startup; when the active output
    device changes (e.g. speakers unplugged) the cached default goes stale and
    opening a stream fails. Terminating and re-initializing refreshes it."""
    try:
        sd._terminate()
        sd._initialize()
    except Exception as e:
        print(f"[tts] portaudio reset failed: {e}", file=sys.stderr)


def _play_worker(text: str) -> None:
    import sounddevice as sd

    samples, sample_rate = load_kokoro().create(text, voice=_voice, speed=KOKORO_SPEED)
    # Synthesis is done — only now interrupt the previous playback so the
    # gap between old and new audio is as small as possible.
    _stop_playback.set()
    sd.stop()
    _stop_playback.clear()
    for attempt in range(2):
        try:
            sd.play(samples, sample_rate)
            break
        except sd.PortAudioError as e:
            # The output device likely changed mid-run; refresh PortAudio's
            # device list and retry once on the new default device.
            print(
                f"[tts] playback error ({e}); resetting audio device", file=sys.stderr
            )
            _reset_portaudio(sd)
            if attempt == 1:
                print("[tts] giving up on this turn's audio", file=sys.stderr)
                return
    while sd.get_stream().active:
        if _stop_playback.is_set():
            sd.stop()
            return
        time.sleep(0.05)


def speak(text: str, label: str) -> None:
    snippet = text.replace("\n", " ")[:80]
    print(f"[tts] {label}: {snippet}{'...' if len(text) > 80 else ''}", file=sys.stderr)
    try:
        summary = summarize(text)
    except Exception as e:
        print(f"[tts] summarize failed: {e}", file=sys.stderr)
        return
    rewarm_async()
    if not summary:
        return
    summary = verbalize(summary)
    print(summary, flush=True)
    try:
        play_tts(summary)
    except Exception as e:
        print(f"[tts] kokoro failed: {e}", file=sys.stderr)


def shutdown() -> None:
    """Stop audio playback and the ollama server we started (if any)."""
    # Stop any in-flight kokoro playback.
    _stop_playback.set()
    try:
        import sounddevice as sd

        sd.stop()
    except Exception:
        pass
    # Stop the ollama server, but only if we were the ones who launched it.
    if _ollama_proc is not None and _ollama_proc.poll() is None:
        print("[tts] stopping ollama server...", file=sys.stderr)
        _ollama_proc.terminate()
        try:
            _ollama_proc.wait(timeout=5)
        except subprocess.TimeoutExpired:
            _ollama_proc.kill()


def main() -> None:
    global _voice
    ensure_server()
    warm_model()
    session = latest_session_file()
    _voice = voice_for_session(session)
    load_kokoro()
    print(f"[tts] watching {session.name} (voice: {_voice})", file=sys.stderr)
    print("[tts] ready, waiting for new turns", file=sys.stderr)
    for line in follow(session):
        text = extract_assistant_text(line)
        if not text:
            continue
        speak(text, "new")


if __name__ == "__main__":
    try:
        main()
    except KeyboardInterrupt:
        pass
    finally:
        shutdown()
