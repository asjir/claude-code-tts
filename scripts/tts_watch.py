#!/usr/bin/env python3
"""Watch active Claude Code and Codex sessions and speak a talkable summary
of each new assistant turn, generated by a local Ollama model.

Tails all currently-active sessions from both tools at once (each keeps its
own read position so no lines are lost); the newest utterance interrupts
whatever is currently speaking. By default Codex turns are read in a random
American voice and Claude turns in a random British voice (the `mixed`
accent); --accent american|british overrides both."""

from __future__ import annotations

import argparse
import hashlib
import json
import sys
import time
from collections.abc import Callable, Iterator
from dataclasses import dataclass, field
from pathlib import Path

import playback
import summarizer
from sessions import classify_codex_turn, classify_turn

PROJECTS_ROOT = Path.home() / ".claude/projects"
CODEX_ROOT = Path.home() / ".codex/sessions"
POLL_SECONDS = 0.5
# Stop tracking a session that hasn't been written to in this long (and has
# been fully read); it's re-added with seek-to-end if it's touched again.
PRUNE_AGE_SECONDS = 300.0

# Kokoro voice pools by accent (a* = American, b* = British).
AMERICAN_VOICES = [
    "af_bella",
    "af_heart",
    "af_jessica",
    "af_kore",
    "af_nicole",
    "af_nova",
    "af_river",
    "af_sarah",
    "af_sky",
    "am_adam",
    "am_echo",
    "am_eric",
    "am_fenrir",
    "am_liam",
    "am_michael",
    "am_onyx",
    "am_puck",
]
BRITISH_VOICES = [
    "bf_alice",
    "bf_emma",
    "bf_isabella",
    "bf_lily",
    "bm_daniel",
    "bm_fable",
    "bm_george",
    "bm_lewis",
]


@dataclass
class Source:
    name: str  # "codex" | "claude"
    root: Path
    glob: str  # claude: "*/*.jsonl"; codex: "**/rollout-*.jsonl"
    classify: Callable[[str], tuple[str, str, bool] | None]
    accent: str  # "american" | "british" (used in mixed mode)


SOURCES = [
    Source("claude", PROJECTS_ROOT, "*/*.jsonl", classify_turn, "british"),
    Source("codex", CODEX_ROOT, "**/rollout-*.jsonl", classify_codex_turn, "american"),
]

# Set from CLI flags in main().
_accent: str = "mixed"
_kokoro_speed: float = 1.0
_only_final: bool = False


def voice_for_session(session_id: str, pool: list[str]) -> str:
    """Pick a stable voice from the session id (JSONL filename stem).

    Uses SHA-256 rather than the built-in hash(), which is salted per process
    and would give a different voice on every run."""
    digest = hashlib.sha256(session_id.encode()).digest()
    index = int.from_bytes(digest, "big") % len(pool)
    return pool[index]


def pool_for(source: Source) -> list[str]:
    """Voice pool for a source under the active accent mode.

    In `mixed` mode each source uses its own accent (Codex American, Claude
    British); `american`/`british` override both sources to one pool."""
    if _accent == "american":
        return AMERICAN_VOICES
    if _accent == "british":
        return BRITISH_VOICES
    return AMERICAN_VOICES if source.accent == "american" else BRITISH_VOICES


def _codex_cwd(path: Path) -> str | None:
    """Read the working directory from a Codex rollout's session_meta line."""
    try:
        with path.open("r", encoding="utf-8", errors="replace") as fh:
            rec = json.loads(fh.readline())
    except (OSError, json.JSONDecodeError):
        return None
    return (rec.get("payload") or {}).get("cwd")


@dataclass
class MultiTailer:
    """Tail every active session across all sources concurrently.

    Each poll cycle, discovers new session files (mtime >= start_time), opens
    them seeked to end (skip backlog, matching the single-file watcher), reads
    all pending lines, and prunes idle handles to bound open fds."""

    query: str | None = None
    start_time: float = field(default_factory=time.time)
    # path -> (source, open handle)
    handles: dict[Path, tuple[Source, object]] = field(default_factory=dict)

    def _matches_query(self, source: Source, path: Path) -> bool:
        if not self.query:
            return True
        q = self.query.lower()
        if source.name == "claude":
            return q in path.parent.name.lower()
        cwd = _codex_cwd(path)
        return cwd is not None and q in cwd.lower()

    def _discover(self) -> None:
        for source in SOURCES:
            if not source.root.exists():
                continue
            for path in source.root.glob(source.glob):
                if path in self.handles:
                    continue
                try:
                    if path.stat().st_mtime < self.start_time:
                        continue
                except OSError:
                    continue
                if not self._matches_query(source, path):
                    continue
                fh = path.open("r", encoding="utf-8", errors="replace")
                fh.seek(0, 2)  # skip backlog; only react to new turns
                self.handles[path] = (source, fh)
                voice = voice_for_session(path.stem, pool_for(source))
                print(
                    f"[tts] tracking {source.name}/{path.stem} (voice: {voice})",
                    file=sys.stderr,
                )

    def _prune(self) -> None:
        cutoff = time.time() - PRUNE_AGE_SECONDS
        for path in list(self.handles):
            _, fh = self.handles[path]
            try:
                mtime = path.stat().st_mtime
            except OSError:
                mtime = 0.0
            if mtime < cutoff:
                fh.close()
                del self.handles[path]

    def follow(self) -> Iterator[tuple[Source, str, str]]:
        """Yield (source, session_id, line) as lines are appended anywhere.

        session_id is the file stem: Claude UUIDs and Codex `rollout-…` stems
        never collide, so the summarizer can key one history per session."""
        while True:
            self._discover()
            for path, (source, fh) in list(self.handles.items()):
                while True:
                    line = fh.readline()
                    if not line:
                        break
                    yield source, path.stem, line
            self._prune()
            time.sleep(POLL_SECONDS)


def speak(session_id: str, text: str, label: str, is_final: bool, voice: str) -> None:
    snippet = text.replace("\n", " ")[:80]
    print(f"[tts] {label}: {snippet}{'...' if len(text) > 80 else ''}", file=sys.stderr)
    try:
        summary = summarizer.summarize(session_id, text, is_final)
    except Exception as e:
        print(f"[tts] summarize failed: {e}", file=sys.stderr)
        return
    if not summary:
        return
    summary = summarizer.verbalize(summary)
    print(summary, flush=True)
    try:
        playback.play(summary, voice, _kokoro_speed)
    except Exception as e:
        print(f"[tts] kokoro failed: {e}", file=sys.stderr)


def shutdown() -> None:
    """Stop audio playback and the ollama server we started (if any)."""
    playback.stop()
    summarizer.shutdown()


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Watch active Claude Code and Codex sessions and speak summaries."
    )
    parser.add_argument(
        "query",
        nargs="?",
        help="Substring to scope which sessions to watch: matches Claude "
        "project-dir names and Codex session working directories.",
    )
    parser.add_argument(
        "--only-final",
        action="store_true",
        help="Only speak the final assistant message of each turn "
        "(Claude stop_reason=end_turn / Codex final_answer); skip updates.",
    )
    parser.add_argument(
        "--accent",
        choices=("mixed", "american", "british"),
        default="mixed",
        help="Voice accent. mixed (default): Codex American, Claude British; "
        "american/british force one pool for both sources.",
    )
    parser.add_argument(
        "--speed",
        type=float,
        default=1.0,
        help="Speech speed multiplier (default: 1.0).",
    )
    return parser.parse_args()


def main() -> None:
    global _accent, _kokoro_speed, _only_final
    ns = parse_args()
    _accent = ns.accent
    _kokoro_speed = ns.speed
    _only_final = ns.only_final
    if ns.query:
        print(f"[tts] scoped to sessions matching {ns.query!r}", file=sys.stderr)
    summarizer.ensure_server()
    summarizer.warm_model()
    playback.load()
    print(f"[tts] accent: {_accent}", file=sys.stderr)
    print("[tts] ready, waiting for new turns", file=sys.stderr)
    tailer = MultiTailer(query=ns.query)
    for source, session_id, line in tailer.follow():
        turn = source.classify(line)
        if turn is None:
            continue
        kind, text, is_final = turn
        if kind == "human_prompt":
            print(
                f"[tts] new prompt in {source.name}/{session_id[:8]}, resetting",
                file=sys.stderr,
            )
            summarizer.begin_user_prompt(session_id, text)
        elif kind == "assistant_text":
            if _only_final and not is_final:
                continue
            voice = voice_for_session(session_id, pool_for(source))
            speak(session_id, text, source.name, is_final, voice)


if __name__ == "__main__":
    try:
        main()
    except KeyboardInterrupt:
        pass
    finally:
        shutdown()
